//Задача 2. Почему 6.35.toFixed(1) == 6.3?
/*Методы Math.round и toFixed, согласно документации, округляют до ближайшего целого 
числа: 0..4 округляется в меньшую сторону, тогда как 5..9 в большую сторону.
Например:
alert( 1.35.toFixed(1) ); // 1.4
Но почему в примере ниже 6.35 округляется до 6.3?
alert( 6.35.toFixed(1) ); // 6.3
Как правильно округлить 6.35?*/
{
	alert( Math.round( (6.35 * 10).toFixed(10)) / 10 );
}

//Задача 3. Ввод числового значения
/*Создайте функцию readNumber, которая будет запрашивать ввод числового значения до 
тех пор, пока посетитель его не введёт. Функция должна возвращать числовое значение.
Также надо разрешить пользователю остановить процесс ввода, отправив пустую строку или 
нажав «Отмена». В этом случае функция должна вернуть null.*/

{
	function readNumber() {
		let num = prompt("Введите число", '');

		if (num === null || num === '') return null;
		
		while (!isFinite(num)) {
			num = +prompt("Введите число", '');
		} 

		return num;
	}
	alert(`Число: ${readNumber()}`);
}

